import java.util.*;

public class AplusBminus3 {
    public static void main(String[] args) {
        Scanner ms = new Scanner(System.in);
        
       
        int T = ms.nextInt();

        for(int i = 0; i < T; i++){
            int A = ms.nextInt();
            int B = ms.nextInt();
            System.out.println(A + B);
        }
        ms.close();
    }
}
